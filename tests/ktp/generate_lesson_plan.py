def test_generate_lesson_plan(driver, wait, base_url, login, shared_state):
    lesson_url = shared_state.get("lesson_url")
    assert lesson_url, "lesson_url not set — run test_create_lesson first"

    ktp_name = shared_state.get("ktp_name")
    assert ktp_name, "ktp_name not set — run test_create_ktp first"
    assert ktp_name in LESSON_DATA, f"No lesson data for KTP: {ktp_name!r}"

    data = LESSON_DATA[ktp_name]

    driver.get(lesson_url)

    lesson = LessonPage(driver, wait, base_url)
    lesson.click_generate()

    lesson.fill_learning_objectives(data["learning_obj"])
    lesson.fill_lesson_goal(data["lesson_goal"])
    lesson.fill_value(data["value"])
    lesson.fill_assessment_criteria(data["assessment"])
    lesson.fill_special_needs(data["special_needs"])

    lesson.generate_plan()

    # ── Verify the editor is not empty ────────────────────────────────────
    # After generation, the "Поурочный план" card should show "Готово".
    # Clicking "Редактировать" opens the lesson plan editor; the editor
    # must contain non-empty content — if it is blank the AI produced nothing.
    lesson.wait_for_plan_ready()
    lesson.click_edit_lesson_plan()

    editor = LessonPlanEditorPage(driver, wait, base_url)
    texts = editor.get_editor_texts()
    assert texts, (
        f"No contenteditable regions found on the lesson plan editor page.\n"
        f"Current URL: {driver.current_url}\n"
        f"Page text snippet: {editor.get_full_page_text()[:500]!r}"
    )
    assert any(t for t in texts), (
        f"Lesson plan editor is empty — AI generation produced no content.\n"
        f"Current URL: {driver.current_url}\n"
        f"Editor regions ({len(texts)}): {texts!r}"
    )

    # Signal to downstream tests (e.g. generate_presentation) that the
    # КСП has been successfully generated for the current lesson.
    shared_state["lesson_plan_generated"] = True
    shared_state["lesson_plan_url"] = driver.current_url
